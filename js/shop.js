var total = 0;
var dem = 0;
function addItem1(){
    var text = document.getElementById('money1').value;
    var number = text.match(/\d/g);
    number = number.join("");
    total += parseFloat(number);
    dem++;
    document.getElementById('dem').innerHTML= dem;
}
